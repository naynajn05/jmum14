package com.tnsif.loopdemo;

public class ForEachDemo {

	public static void main(String[] args) {
		int[] intarr = { 10, 20, 30, 30, 50 };

		for (int i : intarr) {
			System.out.println(i);
		}
	

	String[] names= {"Sanika", "Ansh","Manish"};
	for (String i : names) {
		System.out.println(i);
	}
}
}

